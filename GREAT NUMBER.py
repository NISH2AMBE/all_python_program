# 2 NUMBER GREATER

a = int(input("Enter Your Frist Number :-->"))
b = int(input("Enter Your Second Number :-->"))

if b < a:
    print(f"Frist Number {a} is Greater")
elif a < b:
    print(f"Second Number {b} is Greater")

else:
    print(f"Your Frist Number {a} And Second Number {b} Are Same")


